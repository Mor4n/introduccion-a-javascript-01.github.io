// igualdad debil ==
console.log("1==1",1==1);
console.log("0==\"\"",0==""); // da true, aunque no sea el mismo tipo de dato

// igualdad estricta ===
console.log("1===1",1===1);
console.log("0==\"\"",0===""); // va a revisar que sea el mismo tipo de dato

// desigualdad debil !=
console.log("1!=2",1!=2);
console.log("undefined != null",undefined != null); // false, pq es tipado debil

// desigualdad estricta !==
console.log("1!==2",1!==2);
console.log("undefined != null",undefined !== null);

// Mayor que >
console.log("3>2", 3>2);
// Menor que <
console.log("3<2", 3<2);
