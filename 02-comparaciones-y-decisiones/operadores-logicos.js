// AND (y) && Si esto Y ESTO se cumple, será true
console.log("true && true", true && true); 

console.log("5<10 && 5>=10",5<10 && 5>=10);

console.log("5<10 && 30>=10",5<10 && 30>=10);



// OR (o) || Si cualquiera de los dos se cumple, será true

console.log("false || true",false || true);

console.log("5>10 || 5>5",5>10 || 5>5);

console.log("20<5 || 10>2", 20<5 || 10>2);


