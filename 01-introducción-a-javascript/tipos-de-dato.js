// Ejemplos de tipos de datos en JavaScript

// String: texto entre comillas
console.log(typeof 'Veinticinco');
console.log(typeof '');

// Number: numeros enteros o decimales
console.log(typeof 42);
console.log(typeof -666);
console.log(typeof 0);

// Boolean: verdadero o falso
console.log(typeof true);

// Null: valor nulo
console.log(typeof null);

// Undefined: variable sin valor asignado
console.log(typeof undefined);
console.log(typeof FALSE);


// Estos son unos de mi parte que quise probar ;u;
console.log("De mi parte son los siguientes ;u;");

// String
console.log(typeof "Holaaa con una comilla doble"); // string comilla doble
console.log(typeof ""); // comilla doble
console.log(typeof ''); // comilla simple

// Number
console.log(typeof 21.32); // positivo con decimal
console.log(typeof -922.173); // negativo con decimal

// NaN: Es como cuando queremos hacer una operación invalida, como querer hacer una operación matematica con entre un texto y un numero, su tipo es Number
console.log(typeof NaN); // not a number o no es un numero

// Boolean
console.log(typeof false); // false en minusculas


// BigInt: Números enteros muy grandes
console.log(typeof 1n); // con el n se hace bigint

// Symbol: Como para identificadores unicos o ids
let simbolo = Symbol("holaaa soy un simbolo creo");
console.log(typeof simbolo); // simbolo
